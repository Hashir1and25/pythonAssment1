#Q# 01: Write a program that calculates the sum, difference, product, and average of three numbers:




# Define the three numbers
num1 = 1
num2 = 20
num3 = 30

# Calculate the sum
sum_numbers = num1 + num2 + num3

# Calculate the difference
difference_numbers = num1 - num2 - num3

# Calculate the product
product_numbers = num1 * num2 * num3

# Calculate the average
average_numbers = sum_numbers / 3

# Print the results
print(f"Sum: {sum_numbers}")
print(f"Difference: {difference_numbers}")
print(f"Product: {product_numbers}")
print(f"Average: {average_numbers}")


#Q# 02: Write Python code to calculate the area of the following shapes:
# Area of triangle
# Define base and height
base = 5
height = 10

# Calculate area of the triangle
area_triangle = 0.5 * base * height

# Print the result
print(f"Area of the triangle: {area_triangle}")


# Area of square


# Define the side length
side = 4

# Calculate area of the square
area_square = side * side

# Print the result
print(f"Area of the square: {area_square}")


# Area of circle

import math

# Define the radius
radius = 7

# Calculate area of the circle
area_circle = math.pi * (radius ** 2)

# Print the result
print(f"Area of the circle: {area_circle}")


# area of rectangle

